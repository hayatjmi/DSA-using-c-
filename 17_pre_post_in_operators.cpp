#include<iostream>
using namespace std;
int main()
{
               int a =10;
               int b;
               b=a++; //post increament
               cout<<a<<"\n"<<b<<endl;
               int x=10;
               int y;
               y=++x; //pre increament
               cout<<x<<"\n"<<y<<endl; //we can also do the same with -- sign for decreaments
               return 0;
                            
}